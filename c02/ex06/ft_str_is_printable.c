int	ft_str_is_printable(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] <= 126 && str[i] >= 32))
		{
			return (0);
		}
		i++;
	}
	if (str[i] == '\0')
		return (1);
	else
		return (1);
}
