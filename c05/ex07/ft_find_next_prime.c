int	ft_find_next_prime(int nb)
{
	if (nb < 2)
		return (0);
	if (nb == 2 || nb == 3 || nb == 5 || nb == 7)
	{
		return (nb);
	}
	while (nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0 || nb % 7 == 0)
	{
		++nb;
	}
	return (nb);
}
