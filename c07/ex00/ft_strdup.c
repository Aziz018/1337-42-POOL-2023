#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*duplicate;

	i = 0;
	while (src[i])
	{
		i++;
	}
	duplicate = (char *)malloc((i) * sizeof(char));
	if (duplicate == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		duplicate[i] = src[i];
		i++;
	}
	duplicate[i] = '\0';
	return (duplicate);
}
